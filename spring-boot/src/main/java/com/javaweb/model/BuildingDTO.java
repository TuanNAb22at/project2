package com.javaweb.model;

public class BuildingDTO {
	private String name, managername, managerphone, type;
	private int floorarea, numberofbasement, rentpriceFrom, rentpriceTo, staffid, areaFrom, areaTo;
	private String district;
	private String ward;
	private String street;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManagername() {
		return managername;
	}
	public void setManagername(String managername) {
		this.managername = managername;
	}
	public String getManagerphone() {
		return managerphone;
	}
	public void setManagerphone(String managerphone) {
		this.managerphone = managerphone;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getFloorarea() {
		return floorarea;
	}
	public void setFloorarea(int floorarea) {
		this.floorarea = floorarea;
	}
	public int getNumberofbasement() {
		return numberofbasement;
	}
	public void setNumberofbasement(int numberofbasement) {
		this.numberofbasement = numberofbasement;
	}
	public int getRentpriceFrom() {
		return rentpriceFrom;
	}
	public void setRentpriceFrom(int rentpriceFrom) {
		this.rentpriceFrom = rentpriceFrom;
	}
	public int getRentpriceTo() {
		return rentpriceTo;
	}
	public void setRentpriceTo(int rentpriceTo) {
		this.rentpriceTo = rentpriceTo;
	}
	public int getStaffid() {
		return staffid;
	}
	public void setStaffid(int staffid) {
		this.staffid = staffid;
	}
	public int getAreaFrom() {
		return areaFrom;
	}
	public void setAreaFrom(int areaFrom) {
		this.areaFrom = areaFrom;
	}
	public int getAreaTo() {
		return areaTo;
	}
	public void setAreaTo(int areaTo) {
		this.areaTo = areaTo;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getWard() {
		return ward;
	}
	public void setWard(String ward) {
		this.ward = ward;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	
	
	
}
